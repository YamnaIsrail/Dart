//IMPORTANT:
/* in the parameter of switch you can either pass String value or int
    and double and boolean are not allowed
*/

void main()
{
  var grade= "A";


  switch(grade)
      {
    case 'A' :
      print("Grade A");
      break;

    case 'B' :
      print("Grade B");
      break;

    case 'C' :
      print("Grade C");
      break;

    case 'D' :
      print("Grade D");
      break;

    case 'E' :
      print("Grade E");
      break;

    default:
      print("Invalid");
      break;

  }
}
